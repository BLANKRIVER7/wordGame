package javawin01;

public class day01 {

	public static void main(String[] args) {
		int a = 0; //선언문 변수 이름이 a 초기값 0
		int b = 0; // 선언문 변수 이름이 b 초기값 0
		a = 30; // 30을 a변수 값에 대입
		b = 50; // 50을 b변수 값에 대입
		int c = 0; // 선언문  변수 이름이 c 초기값 0
		c = a + b; // 변수 a와 b의 값을 더해서 변수 c에 대입
		System.out.println(c); // c변수 값을 콘솔에 출력
		//SYstem.out.println() '명령어는 콘송에 출력하라'이다.
	}

}
